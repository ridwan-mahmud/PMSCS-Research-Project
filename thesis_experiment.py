def build_cnn(vocab_size, max_len, embed_dim=300):

    inp = Input(shape=(max_len,))
    emb = Embedding(vocab_size, embed_dim, input_length=max_len)(inp)

    c2 = GlobalMaxPooling1D()(Conv1D(128, 2, activation='relu')(emb))
    c3 = GlobalMaxPooling1D()(Conv1D(128, 3, activation='relu')(emb))
    c4 = GlobalMaxPooling1D()(Conv1D(128, 4, activation='relu')(emb))

    merged = Concatenate()([c2, c3, c4])
    x = BatchNormalization()(merged)
    x = Dropout(0.5)(x)
    x = Dense(128, activation='relu')(x)
    x = Dropout(0.3)(x)
    out = Dense(1, activation='sigmoid')(x)

    model = Model(inputs=inp, outputs=out)
    model.compile(optimizer='adam', loss='binary_crossentropy', metrics=['accuracy'])
    return model


def train_cnn(X_tr, X_te, y_tr, y_te, lang):

    print(f"\n⏳ Training Custom CNN ({lang})...")

    tokenizer = Tokenizer(num_words=50000, oov_token="<OOV>")
    tokenizer.fit_on_texts(X_tr)

    X_tr_seq = tokenizer.texts_to_sequences(X_tr)
    X_te_seq = tokenizer.texts_to_sequences(X_te)

    lengths = [len(s) for s in X_tr_seq]
    max_len = int(np.percentile(lengths, 99))
    print(f"  Max Sequence Length (99th pct): {max_len}")

    X_tr_pad = pad_sequences(X_tr_seq, maxlen=max_len, padding='post')
    X_te_pad = pad_sequences(X_te_seq, maxlen=max_len, padding='post')

    vocab_size = min(len(tokenizer.word_index) + 1, 50000)
    print(f"  Vocab Size: {vocab_size}")

    model = build_cnn(vocab_size, max_len)

    callbacks = [
        EarlyStopping(monitor='val_loss', patience=5, restore_best_weights=True),
        ReduceLROnPlateau(monitor='val_loss', factor=0.5, patience=3)
    ]

    t0 = time.time()
    history = model.fit(
        X_tr_pad, y_tr,
        epochs=50, batch_size=64,
        validation_split=0.1,
        callbacks=callbacks,
        verbose=1
    )
    train_t = time.time() - t0

    print(f"  Training Time: {round(train_t,1)}s")
    print(f"  Epochs: {len(history.history['loss'])}")

    t0 = time.time()
    y_prob = model.predict(X_te_pad, verbose=0)
    inf_t = time.time() - t0

    y_pred = (y_prob.flatten() >= 0.5).astype(int)

    result = evaluate_model(y_te, y_pred, "Custom CNN", lang, inf_t)
    result["training_time_s"] = round(train_t, 2)
    result["epochs_trained"] = len(history.history['loss'])

    # Plot training history
    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(12, 4))

    ax1.plot(history.history['loss'], label='Train')
    ax1.plot(history.history['val_loss'], label='Val')
    ax1.set_title(f'Loss ({lang})')
    ax1.set_xlabel('Epoch')
    ax1.legend()

    ax2.plot(history.history['accuracy'], label='Train')
    ax2.plot(history.history['val_accuracy'], label='Val')
    ax2.set_title(f'Accuracy ({lang})')
    ax2.set_xlabel('Epoch')
    ax2.legend()

    plt.tight_layout()
    plt.savefig(f"{RESULTS_DIR}/cnn_history_{lang.lower()}.png", dpi=150)
    plt.show()
    plt.close()

    return result, y_pred




def run_experiment(file_path, language, balance=False):

    results = []

    # Load data
    X_train_raw, X_test_raw, y_train, y_test, stats = load_and_prepare_data(
        file_path, language, balance
    )

    # Preprocess
    print(f"\n📝 Preprocessing {language} text...")
    X_train = np.array([preprocess_text(t, language) for t in X_train_raw])
    X_test = np.array([preprocess_text(t, language) for t in X_test_raw])

    
    test_df = pd.DataFrame({'headline': X_test, 'label': y_test})
    test_df.to_csv(f"{RESULTS_DIR}/test_set_{language.lower()}.csv", index=False)
    print(f"  Test set saved: {RESULTS_DIR}/test_set_{language.lower()}.csv")

    print("\n📊 Creating TF-IDF features...")
    X_tr_tfidf, X_te_tfidf, vec = create_tfidf_features(X_train, X_test)

    # Train all models
    models_to_train = [
        ("Naive Bayes", train_naive_bayes),
        ("Logistic Regression", train_logistic_regression),
        ("SVM", train_svm),
        #("Random Forest", train_random_forest),
        ("XGBoost", train_xgboost),
    ]

    for name, train_func in models_to_train:
        result, y_pred = train_func(X_tr_tfidf, X_te_tfidf, y_train, y_test, language)
        results.append(result)
        plot_confusion_matrix(y_test, y_pred, name, language)

  
    result, y_pred = train_cnn(X_train, X_test, y_train, y_test, language)
    results.append(result)
    plot_confusion_matrix(y_test, y_pred, "Custom CNN", language)

    # Comparison chart
    plot_comparison(results, language)

    return results, stats


