# ============================================
# GROQ API CALL
# ============================================

def generate_label(headline, prompt_template, model_id, max_retries=3):
    """Call Groq API for a single headline"""

    prompt = prompt_template.format(headline=str(headline))

    for attempt in range(max_retries):
        try:
            response = client.chat.completions.create(
                model=model_id,
                messages=[
                    {
                        "role": "user",
                        "content": prompt
                    }
                ],
                max_tokens=10,
                temperature=0.0,
                top_p=1.0,
                stream=False
            )
            return response.choices[0].message.content.strip()

        except Exception as e:
            error_msg = str(e)

            # Rate limit error — wait and retry
            if "rate_limit" in error_msg.lower() or "429" in error_msg:
                wait_time = 10 * (attempt + 1)
                print(f"\n  ⚠️ Rate limit hit. Waiting {wait_time}s...")
                time.sleep(wait_time)

            # Other error
            else:
                print(f"\n  ⚠️ API Error (attempt {attempt+1}): {error_msg[:50]}")
                time.sleep(3)

    return "ERROR"


# ============================================
# LABEL PARSER
# ============================================

def parse_label(raw_output):
    """Parse model output to binary label"""

    output_lower = raw_output.lower().strip()

    # Check Not Clickbait first (important order)
    if "not clickbait" in output_lower or "not_clickbait" in output_lower:
        return 0    # Non-clickbait
    elif "clickbait" in output_lower:
        return 1    # Clickbait
    else:
        return -1   # Unparseable


# ============================================
# MAIN INFERENCE FUNCTION
# ============================================

def run_inference(headlines, true_labels, prompt_template,
                  model_name, model_id, prompt_name):
    """Run zero-shot inference on all samples"""

    print(f"\n{'='*65}")
    print(f"  Model:   {model_name} ({model_id})")
    print(f"  Prompt:  {prompt_name}")
    print(f"  Samples: {len(headlines)}")
    print(f"{'='*65}")

    predictions = []
    raw_outputs = []
    unparseable_count = 0
    unparseable_samples = []
    start_time = time.time()

    for i, headline in enumerate(headlines):

        # Call API
        raw = generate_label(headline, prompt_template, model_id)
        raw_outputs.append(raw)

        # Parse output
        pred = parse_label(raw)

        # Handle unparseable
        if pred == -1:
            unparseable_count += 1
            unparseable_samples.append({
                "index": i,
                "headline": str(headline),
                "raw_output": raw
            })
            # Count as wrong prediction (conservative)
            pred = 1 - int(true_labels[i])

        predictions.append(pred)

        # Small delay to respect rate limits
        time.sleep(0.1)

        # Progress update every 100 samples
        if (i + 1) % 100 == 0 or (i + 1) == len(headlines):
            elapsed = time.time() - start_time
            speed = (i + 1) / elapsed
            eta = (len(headlines) - i - 1) / speed if speed > 0 else 0
            print(f"  [{i+1}/{len(headlines)}] "
                  f"Speed: {speed:.1f}/s | "
                  f"ETA: {eta/60:.1f} min | "
                  f"Unparseable: {unparseable_count}")

    total_time = time.time() - start_time
    predictions = np.array(predictions)
    true_labels_arr = np.array(true_labels, dtype=int)

    # ---- Calculate All Metrics ----
    acc = accuracy_score(true_labels_arr, predictions)
    prec = precision_score(true_labels_arr, predictions, average='macro', zero_division=0)
    rec = recall_score(true_labels_arr, predictions, average='macro', zero_division=0)
    f1 = f1_score(true_labels_arr, predictions, average='macro', zero_division=0)
    cm = confusion_matrix(true_labels_arr, predictions)
    f1_per = f1_score(true_labels_arr, predictions, average=None, zero_division=0)

    # Per class precision and recall
    prec_per = precision_score(true_labels_arr, predictions, average=None, zero_division=0)
    rec_per = recall_score(true_labels_arr, predictions, average=None, zero_division=0)

    result = {
        "model_name": model_name,
        "model_id": model_id,
        "prompt": prompt_name,
        "accuracy": round(acc * 100, 2),
        "precision_macro": round(prec * 100, 2),
        "recall_macro": round(rec * 100, 2),
        "f1_macro": round(f1 * 100, 2),
        "f1_non_clickbait": round(f1_per[0] * 100, 2) if len(f1_per) > 0 else 0,
        "f1_clickbait": round(f1_per[1] * 100, 2) if len(f1_per) > 1 else 0,
        "precision_non_clickbait": round(prec_per[0] * 100, 2) if len(prec_per) > 0 else 0,
        "precision_clickbait": round(prec_per[1] * 100, 2) if len(prec_per) > 1 else 0,
        "recall_non_clickbait": round(rec_per[0] * 100, 2) if len(rec_per) > 0 else 0,
        "recall_clickbait": round(rec_per[1] * 100, 2) if len(rec_per) > 1 else 0,
        "confusion_matrix": cm.tolist(),
        "unparseable_count": unparseable_count,
        "unparseable_rate": round(unparseable_count / len(headlines) * 100, 2),
        "total_time_s": round(total_time, 2),
        "inference_time_ms": round(total_time * 1000 / len(headlines), 2),
        "total_samples": len(headlines)
    }

    # ---- Print Results ----
    print(f"\n  {'─'*55}")
    print(f"  ✅ RESULTS: {model_name} | {prompt_name}")
    print(f"  {'─'*55}")
    print(f"  Accuracy:          {result['accuracy']}%")
    print(f"  Precision (Macro): {result['precision_macro']}%")
    print(f"  Recall (Macro):    {result['recall_macro']}%")
    print(f"  F1 (Macro):        {result['f1_macro']}%")
    print(f"  F1 (Clickbait):    {result['f1_clickbait']}%")
    print(f"  F1 (Non-CB):       {result['f1_non_clickbait']}%")
    print(f"  Unparseable:       {result['unparseable_count']} ({result['unparseable_rate']}%)")
    print(f"  Total Time:        {result['total_time_s']}s")
    print(f"  Per Sample:        {result['inference_time_ms']} ms/sample")
    print(f"\n  Confusion Matrix:")
    print(f"  {cm}")

    return result, predictions, raw_outputs, unparseable_samples


