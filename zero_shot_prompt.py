def plot_cm(cm_array, model_name, prompt_name, accuracy):
    """Plot and save confusion matrix"""

    plt.figure(figsize=(6, 5))
    sns.heatmap(
        cm_array,
        annot=True,
        fmt='d',
        cmap='Blues',
        xticklabels=['Non-Clickbait', 'Clickbait'],
        yticklabels=['Non-Clickbait', 'Clickbait']
    )
    plt.title(f'{model_name} — {prompt_name}\nAccuracy: {accuracy}%')
    plt.ylabel('Actual')
    plt.xlabel('Predicted')
    plt.tight_layout()

    safe_model = model_name.replace(" ", "_").replace("-", "_").lower()
    safe_prompt = prompt_name.replace(" ", "_").lower()
    save_path = f"{RESULTS_DIR}/cm_{safe_model}_{safe_prompt}.png"
    plt.savefig(save_path, dpi=150)
    plt.show()
    plt.close()
    print(f"  💾 Saved: {save_path}")


