# COMPARISON BAR CHART
# ============================================

def plot_comparison(results_list, title, save_name):
    """Plot bar chart comparing models"""

    labels = [f"{r['model_name']}\n({r['prompt']})" for r in results_list]
    accuracies = [r['accuracy'] for r in results_list]
    f1_scores = [r['f1_macro'] for r in results_list]

    x = np.arange(len(labels))
    width = 0.35

    fig, ax = plt.subplots(figsize=(12, 6))
    bars1 = ax.bar(x - width/2, accuracies, width, label='Accuracy', color='#2196F3')
    bars2 = ax.bar(x + width/2, f1_scores, width, label='F1 Macro', color='#4CAF50')

    # Add value labels
    for bar in bars1:
        ax.text(bar.get_x() + bar.get_width()/2, bar.get_height() + 0.3,
                f'{bar.get_height()}%', ha='center', va='bottom', fontsize=9)
    for bar in bars2:
        ax.text(bar.get_x() + bar.get_width()/2, bar.get_height() + 0.3,
                f'{bar.get_height()}%', ha='center', va='bottom', fontsize=9)

    ax.set_xlabel('Model and Prompt')
    ax.set_ylabel('Score (%)')
    ax.set_title(title)
    ax.set_xticks(x)
    ax.set_xticklabels(labels, fontsize=9)
    ax.legend()
    ax.set_ylim(0, 110)
    ax.grid(axis='y', alpha=0.3)

    plt.tight_layout()
    save_path = f"{RESULTS_DIR}/{save_name}.png"
    plt.savefig(save_path, dpi=150)
    plt.show()
    plt.close()
    print(f"  💾 Chart saved: {save_path}")


